#include "Widget.hxx"
#include "Class.hxx"

Widget::Widget(struct pool &_pool,
	       const WidgetClass *_cls) noexcept
	:pool(_pool), cls(_cls)
{
	if (_cls != nullptr)
		from_template.view = from_request.view = &_cls->views;
}

Widget::~Widget() noexcept
{
	DiscardForFocused();
	children.clear_and_dispose(Disposer{});
}
